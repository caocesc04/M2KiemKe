    {
        if ($request->password === $request->repassword) {
            $usr = new User;
            $usr->Username = $request->username;
            $usr->Password = $request->password;
            $usr->Ten = $request->Ten;
            $usr->Permission = '1';
            $usr->Status = '1';
            $usr->save();
            return Redirect::view('adduser');
        } else {
            return Redirect::back()->with('error','Mật khẩu không trùng khớp!');
        }
    }

